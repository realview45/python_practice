# 산술연산
print(1+1)# 2
print(3-2)# 1
print(5*2)# 10
print(6/3)#나누기 2.0?
print(5%3)#나머지 2
print(10%3)#1
print(5//3)#몫 1

# 비교연산
print(10 > 3) #True
print(4 >= 7) #False
print(10 < 3) #False
print(3 >= 3) #True

print(3 == 3) #True
print(4 == 2) #False
print(3 + 4 == 7) #True

print(1 != 3) #True
print(not(1!=3)) #False

print((3>0) and (3<5)) #True
print((3>0) & (3<5)) #True

print((3>0) or (3>5)) #True
print((3>0) | (3>5)) #True

#5는 4보다크고 4는 3보다크다
print(5 > 4 > 3) #True
print(5 > 4 > 7) #False

